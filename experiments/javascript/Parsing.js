function display1() {
    var base = document.getElementById("gm");
    var game1 = base.getElementsByTagName("game")[0];
    var gname1 = "game-name :" + game1.getElementsByTagName("gname")[0].firstChild.data;
    var rating1 = "rating :" + game1.getElementsByTagName("rating")[0].firstChild.data;
    var website1 = "website :" + game1.getElementsByTagName("website")[0].firstChild.data;
    var type1 = "type :" + game1.getElementsByTagName("type")[0].firstChild.data;
    alert("game-info : \n \n" + gname1 + "\n" + rating1 + "\n" + website1 + "\n" + type1 + "\n");
}
